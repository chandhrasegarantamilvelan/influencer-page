"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import { signOut } from "next-auth/react";
import { useState } from "react";

const navItems = [
  {
    label: "Overview",
    href: "/dashboard",
    icon: OverviewIcon,
  },
  {
    label: "Requests",
    href: "/dashboard/requests",
    icon: RequestsIcon,
  },
  {
    label: "Portfolio",
    href: "/dashboard/portfolio",
    icon: PortfolioIcon,
  },
  {
    label: "Profiles",
    href: "/dashboard/profiles",
    icon: ProfilesIcon,
  },
];

export function DashboardSidebar() {
  const pathname = usePathname();
  const [isLoggingOut, setIsLoggingOut] = useState(false);
  const [isMobileOpen, setIsMobileOpen] = useState(false);

  async function handleLogout() {
    setIsLoggingOut(true);
    await signOut({ redirectTo: "/login" });
  }

  return (
    <>
      {/* Mobile menu button */}
      <button
        type="button"
        onClick={() => setIsMobileOpen(!isMobileOpen)}
        className="fixed top-4 left-4 z-50 lg:hidden bg-white border border-gray-200 rounded-lg p-2 shadow-sm hover:bg-gray-50 transition-colors"
        aria-label={isMobileOpen ? "Close menu" : "Open menu"}
      >
        {isMobileOpen ? (
          <CloseIcon className="w-5 h-5 text-cabernet" />
        ) : (
          <MenuIcon className="w-5 h-5 text-cabernet" />
        )}
      </button>

      {/* Mobile overlay */}
      {isMobileOpen && (
        <div
          className="fixed inset-0 bg-black/30 z-40 lg:hidden"
          onClick={() => setIsMobileOpen(false)}
          aria-hidden="true"
        />
      )}

      {/* Sidebar */}
      <aside
        className={`
          fixed top-0 left-0 z-40 h-full w-64 bg-white border-r border-gray-100
          shadow-[4px_0_20px_rgba(0,0,0,0.03)] flex flex-col
          transition-transform duration-300 ease-in-out
          lg:translate-x-0 lg:static lg:z-auto
          ${isMobileOpen ? "translate-x-0" : "-translate-x-full"}
        `}
      >
        {/* Logo / Brand */}
        <div className="px-6 py-6 border-b border-gray-100">
          <h2 className="text-lg font-bold text-cabernet tracking-tight">
            Dashboard
          </h2>
          <p className="text-xs text-gray-400 mt-0.5">Admin Panel</p>
        </div>

        {/* Navigation */}
        <nav className="flex-1 px-3 py-4 space-y-1" aria-label="Dashboard navigation">
          {navItems.map((item) => {
            const isActive =
              item.href === "/dashboard"
                ? pathname === "/dashboard"
                : pathname.startsWith(item.href);

            return (
              <Link
                key={item.href}
                href={item.href}
                onClick={() => setIsMobileOpen(false)}
                className={`
                  flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium
                  transition-all duration-200 group
                  ${
                    isActive
                      ? "bg-gradient-to-r from-cabernet/5 to-maroon/5 text-cabernet border border-cabernet/10"
                      : "text-gray-600 hover:bg-gray-50 hover:text-cabernet"
                  }
                `}
                aria-current={isActive ? "page" : undefined}
              >
                <item.icon
                  className={`w-5 h-5 flex-shrink-0 transition-colors ${
                    isActive
                      ? "text-cabernet"
                      : "text-gray-400 group-hover:text-cabernet"
                  }`}
                />
                <span>{item.label}</span>
                {isActive && (
                  <span className="ml-auto w-1.5 h-1.5 rounded-full bg-gold" />
                )}
              </Link>
            );
          })}
        </nav>

        {/* Logout */}
        <div className="px-3 py-4 border-t border-gray-100">
          <button
            type="button"
            onClick={handleLogout}
            disabled={isLoggingOut}
            className="
              flex items-center gap-3 w-full px-3 py-2.5 rounded-lg text-sm font-medium
              text-gray-600 hover:bg-red-50 hover:text-red-700
              transition-all duration-200 disabled:opacity-50 disabled:cursor-not-allowed
            "
          >
            <LogoutIcon className="w-5 h-5 flex-shrink-0" />
            <span>{isLoggingOut ? "Signing out..." : "Sign Out"}</span>
          </button>
        </div>
      </aside>
    </>
  );
}

